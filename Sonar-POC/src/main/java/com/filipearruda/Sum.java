package com.filipearruda;

public class Sum {
    public int sum(int a, int b){
        return a + b;
    }

    public double sum(double a, double b){
        return a + b;
    }

    public float sum(float a, float b){
        return a + b;
    }

    public long sum(long a, long b){
        return a + b;
    }
}
